var Model = require("./model");
var utils = require("./utils");

function Theme() {
    Model.call(this, {
        '_type': 'theme',
        '_ludid': 'theme_object'
    });
}

utils.extend(Theme, Model);

Theme.prototype.getCSS = function() {
    return this.get('css', '');
};

module.exports = new Theme();